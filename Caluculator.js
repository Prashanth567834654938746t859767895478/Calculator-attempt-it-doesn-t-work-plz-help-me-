var q1 = prompt("If you want to multiply type * divide / add + subract -");
var num1 = prompt("Pick a Number");
var num2 = prompt("Pick a another Number");
if (q1 = "*"){
    alert(num1 * num2);
};

if (q1 = "/"){
    alert(num1 / num2);
};

if (q1 = "+"){
    alert(num1 + num2);
};

if (q1 = "-"){
    alert(num1 - num2);
};